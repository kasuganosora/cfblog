/**
 * Build script: Convert pinyinjs dict to char->pinyin lookup
 * Run: node scripts/build-pinyin-dict.js
 * Output: src/utils/pinyin-data.js
 */

import { readFileSync, writeFileSync } from 'fs';

// Read the raw dict
const raw = readFileSync(new URL('./pinyin_dict_notone.js', import.meta.url), 'utf-8');

// Extract the JSON object
const match = raw.match(/pinyin_dict_notone\s*=\s*(\{[\s\S]+\});?/);
if (!match) { console.error('Failed to parse dict'); process.exit(1); }

const dict = JSON.parse(match[1]);

// Invert: pinyin->chars => char->pinyin (first match wins = most common reading)
const charMap = {};
for (const [pinyin, chars] of Object.entries(dict)) {
  for (const ch of chars) {
    if (!charMap[ch]) {
      charMap[ch] = pinyin;
    }
  }
}

// Build a compact string: "char pinyin\n" sorted by char code
const entries = Object.entries(charMap).sort((a, b) => a[0].charCodeAt(0) - b[0].charCodeAt(0));

// Encode as a single string for minimal size: "一yi二er三san..."
// Use a Map-based lookup for O(1) access
let dictStr = '';
for (const [ch, py] of entries) {
  dictStr += ch + py + '\n';
}

const output = `// Auto-generated by scripts/build-pinyin-dict.js — do not edit
// Source: https://github.com/sxei/pinyinjs (pinyin_dict_notone)
const D=${JSON.stringify(dictStr)};
const M=new Map();
D.split('\\n').forEach(l=>{if(l.length>=2)M.set(l[0],l.slice(1))});
export function toPinyin(str){
  return String(str||'').split('').map(c=>M.get(c)||c).join('');
}
`;

writeFileSync(new URL('../src/utils/pinyin-data.js', import.meta.url), output);
console.log(`Done: ${entries.length} chars, output size: ${Buffer.byteLength(output)} bytes`);
